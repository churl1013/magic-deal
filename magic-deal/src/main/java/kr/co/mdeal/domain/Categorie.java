package kr.co.mdeal.domain;

public class Categorie {
	private int pCategorieNo;
	private String pKeyword;
	private int pHighCate;
	private int pLowCate;
	
	public int getpCategorieNo() {
		return pCategorieNo;
	}
	public void setpCategorieNo(int pCategorieNo) {
		this.pCategorieNo = pCategorieNo;
	}
	public String getpKeyword() {
		return pKeyword;
	}
	public void setpKeyword(String pKeyword) {
		this.pKeyword = pKeyword;
	}
	public int getpHighCate() {
		return pHighCate;
	}
	public void setpHighCate(int pHighCate) {
		this.pHighCate = pHighCate;
	}
	public int getpLowCate() {
		return pLowCate;
	}
	public void setpLowCate(int pLowCate) {
		this.pLowCate = pLowCate;
	}
}
